export function normalizeState(input: string): string {
  const trimmed = input.trim().toLowerCase();
  const normalized = stateMap[trimmed];
  if (normalized) return normalized;
  if (trimmed.length === 2) return trimmed.toUpperCase(); // allow 2-letter codes
  return input.toUpperCase().slice(0, 2); // fallback
}
